-- ============================================================================
-- Push Subscriptions Table
-- ============================================================================

CREATE TABLE push_subscriptions (
  -- Primary key
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

  -- Ownership
  -- Links subscription to authenticated user
  -- Using auth.users(id) directly (matches profiles/devices table pattern)
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,

  -- Device association
  -- CRITICAL: Push subscriptions are device-scoped (each browser/PWA instance has unique endpoint)
  -- Links to devices table for multi-device push support
  -- Cascade delete ensures cleanup when device is removed
  device_id TEXT NOT NULL REFERENCES devices(id) ON DELETE CASCADE,

  -- Web Push subscription data
  -- These fields are required by Web Push API for message encryption
  -- See: https://developer.mozilla.org/en-US/docs/Web/API/PushSubscription
  endpoint TEXT NOT NULL,   -- Push service URL (unique per subscription)
  p256dh TEXT NOT NULL,     -- Public key for P-256 ECDH encryption (base64-encoded)
  auth TEXT NOT NULL,       -- Authentication secret for message authentication (base64-encoded)

  -- Audit timestamps
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

  -- Constraints
  -- One subscription per device per user (subscriptions are device-scoped)
  -- This prevents duplicate subscriptions when re-registering same device
  UNIQUE(user_id, device_id)
);

