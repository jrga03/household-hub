-- Function: check_budget_thresholds()
-- Purpose: Calculate budget spending vs targets and return budgets >= 80% threshold
-- Usage: Called by Supabase Edge Function for daily budget alert notifications
-- Returns: Budgets that have reached or exceeded 80% of their target amount
--
-- CRITICAL: This function MUST exclude transfers (transfer_group_id IS NULL)
--           to prevent double-counting in budget calculations.
--
-- Design:
-- 1. Join budgets with categories for display names
-- 2. LEFT JOIN transactions to calculate actual spending
-- 3. Use DATE_TRUNC for month matching with 'Asia/Manila' timezone
-- 4. Exclude transfers (WHERE transfer_group_id IS NULL)
-- 5. Sum only expense transactions (type = 'expense')
-- 6. Filter for current/future months only
-- 7. Calculate percentage: (spent_cents * 100 / amount_cents)
-- 8. Return budgets >= 80% threshold ordered by percentage DESC

CREATE OR REPLACE FUNCTION check_budget_thresholds()
RETURNS TABLE (
  id UUID,
  user_id UUID,
  category_id UUID,
  category_name TEXT,
  amount_cents BIGINT,
  spent_cents BIGINT,
  percentage INTEGER
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  RETURN QUERY
  WITH budget_spending AS (
    SELECT
      b.id,
      b.household_id,  -- Added: needed for CROSS JOIN with profiles
      b.category_id,
      b.amount_cents,
      c.name AS category_name,
      -- Calculate actual spending for this budget's month and category
      -- CRITICAL: Exclude transfers to avoid double-counting
      COALESCE(SUM(
        CASE
          WHEN t.type = 'expense' AND t.transfer_group_id IS NULL
          THEN t.amount_cents
          ELSE 0
        END
      ), 0) AS spent_cents
    FROM budgets b
    INNER JOIN categories c ON b.category_id = c.id
    LEFT JOIN transactions t ON
      t.category_id = b.category_id
      -- Match transactions to budget month using Asia/Manila timezone
      -- DATE_TRUNC returns first day of month for both sides
      AND DATE_TRUNC('month', t.date AT TIME ZONE 'Asia/Manila') = DATE_TRUNC('month', b.month AT TIME ZONE 'Asia/Manila')
      -- CRITICAL: Exclude transfers from budget calculations
      -- Transfers are paired transactions (expense + income) that net to zero
      -- Including them would incorrectly inflate spending totals
      AND t.transfer_group_id IS NULL
    WHERE
      -- Only check current and future months (ignore historical budgets)
      b.month >= DATE_TRUNC('month', NOW() AT TIME ZONE 'Asia/Manila')
    GROUP BY b.id, b.household_id, b.category_id, b.amount_cents, c.name
  )
  SELECT
    bs.id,
    -- Get all users in the household (budgets are household-scoped per budgets table design)
    -- Each household member receives the budget alert (aligned with household financial management)
    p.id AS user_id,
    bs.category_id,
    bs.category_name,
    bs.amount_cents,
    bs.spent_cents,
    -- Calculate percentage of budget used
    -- Use integer division after scaling to avoid floating point
    CASE
      WHEN bs.amount_cents > 0
      THEN ((bs.spent_cents * 100) / bs.amount_cents)::INTEGER
      ELSE 0
    END AS percentage
  FROM budget_spending bs
  CROSS JOIN profiles p  -- Alert all household members
  WHERE
    p.household_id = bs.household_id  -- Match household
    AND bs.amount_cents > 0
    AND ((bs.spent_cents * 100) / bs.amount_cents) >= 80  -- 80% threshold
  ORDER BY percentage DESC;
END;
$$;

